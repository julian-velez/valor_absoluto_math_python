import math  # Importa el módulo 'math' que contiene funciones matemáticas avanzadas

# La función math.fabs(x) devuelve el valor absoluto de x como número de tipo float
# Es decir, convierte un número negativo en positivo, pero siempre retorna un float

print(math.fabs(-10))  # Imprime el valor absoluto de -10 → resultado: 10.0

